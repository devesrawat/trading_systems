"""
Phase 6: Audit query functions.

Retrieve audit trail entries by filters (strategy, date, reason, symbol).
Phase 7 will implement full TimescaleDB queries. For now, Redis retrieval.
"""

from __future__ import annotations

from datetime import datetime
from typing import Any

import structlog

from audit.schema import (
    CircuitBreakerLog,
    RiskDecisionLog,
    SignalLogEntry,
    TradeLogEntry,
)
from data.redis_keys import RedisKeys
from data.store import get_redis

log = structlog.get_logger(__name__)


class AuditQuery:
    """Query audit logs by various filters."""

    @staticmethod
    def get_signals_by_strategy(
        strategy: str,
        since: datetime | None = None,
    ) -> list[SignalLogEntry]:
        """
        Get signals generated by a specific strategy.

        Parameters
        ----------
        strategy : str
            Strategy name to filter by
        since : datetime, optional
            Only return signals after this timestamp

        Returns
        -------
        list[SignalLogEntry]
            Matching signal entries
        """
        try:
            entries = []
            redis = get_redis()
            cursor = 0
            pattern = f"{RedisKeys.AUDIT_SIGNALS}:*"

            while True:
                cursor, keys = redis.scan(cursor, match=pattern, count=100)
                for key in keys:
                    try:
                        data = redis.get(key)
                        if data:
                            entry = SignalLogEntry.model_validate_json(data)
                            if entry.strategy_name == strategy and (
                                since is None or entry.timestamp >= since
                            ):
                                entries.append(entry)
                    except Exception:
                        pass
                if cursor == 0:
                    break

            return sorted(entries, key=lambda x: x.timestamp, reverse=True)
        except Exception as exc:
            log.warning("audit_query_signals_failed", error=str(exc))
            return []

