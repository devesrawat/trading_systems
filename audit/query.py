    @staticmethod
    def get_signal_statistics(strategy: str | None = None) -> dict[str, Any]:
        """
        Get signal statistics.

        Parameters
        ----------
        strategy : str, optional
            If provided, only compute stats for this strategy

        Returns
        -------
        dict
            Statistics dictionary
        """
        try:
            redis = get_redis()
            cursor = 0
            pattern = f"{RedisKeys.AUDIT_SIGNALS}:*"
            signals: list[SignalLogEntry] = []

            while True:
                cursor, keys = redis.scan(cursor, match=pattern, count=100)
                for key in keys:
                    try:
                        data = redis.get(key)
                        if data:
                            entry = SignalLogEntry.model_validate_json(data)
                            if strategy is None or entry.strategy_name == strategy:
                                signals.append(entry)
                    except Exception:
                        pass
                if cursor == 0:
                    break

            if not signals:
                return {}

            total = len(signals)
            long_signals = sum(1 for s in signals if s.direction == "long")
            short_signals = sum(1 for s in signals if s.direction == "short")
            executed = sum(1 for s in signals if s.mode in ("paper", "live"))
            avg_confidence = sum(s.confidence for s in signals) / total if signals else 0
            avg_score = sum(s.score for s in signals) / total if signals else 0

            return {
                "total": total,
                "long": long_signals,
                "short": short_signals,
                "executed": executed,
                "avg_confidence": avg_confidence,
                "avg_score": avg_score,
                "execution_rate": executed / total if total > 0 else 0,
            }
        except Exception as exc:
            log.warning("audit_signal_statistics_failed", error=str(exc))
            return {}
